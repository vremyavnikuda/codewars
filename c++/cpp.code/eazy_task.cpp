#include <iostream>
using namespace std;
int main()
{
    float a;
    float b;

    cin >> a;
    cin >> b;
    cout << "Сумма чисел: " << a + b << endl;
    cout << "Разность чисел: " << a - b << endl;
    cout << "Произведение чисел: " << a * b << endl;
    cout << "Частное от деления чисел: " << a / b;
}